    private void cargarClientes(){
        jComboBox1.removeAllItems();
        ArrayList clientes=new mClientes().clientesList();
        for(Object o:clientes){
        jComboBox1.addItem(o.toString());
        }       
    }
    private void actualizar(){
    ArrayList data = new ArrayList();
        for (int i = 0; i < this.jTable1.getRowCount(); i++) {
            ArrayList row = new ArrayList();
            row.add(tabla.getValueAt(i, 0).toString());
            row.add(tabla.getValueAt(i, 1).toString());
            row.add(tabla.getValueAt(i, 2).toString());
            data.add(row);
        }
        if(new mFavoritos().actualizar(data)){
        JOptionPane.showMessageDialog(null,"Base de datos actualizada", "GOOD",JOptionPane.INFORMATION_MESSAGE);
        int a = this.tabla.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            this.tabla.removeRow(i);
        }
        jButton3.setEnabled(false);
        }else{
        JOptionPane.showMessageDialog(null, "Imposible Actualizar", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    private void cargarProductos(){
    ArrayList tab=new mFavoritos().productList(jComboBox1.getSelectedItem().toString());
        for(Object o:tab){
            ArrayList row=(ArrayList)o;
            tabla.addRow(row.toArray());
        }
        jButton3.setEnabled(true);
    }
